#include <iostream>
#include <vector>
using namespace std;

int main() {
  vector<vector<int>> inputs = {{2, 3}, {1, 4}, {6, 7}, {2, 2}, {5,5}, {1, 20}};
  sort(inputs.begin(), inputs.end());
  for (int i = 0; i < inputs.size(); ++i) {
    cout << "{" << inputs[i][0] << "," << inputs[i][1] << "}\n";
  }
  return 0;
}
